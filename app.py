@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(silent=True) or {}
    text = data.get("text")
    creator_id = data.get("creator_id")

    if not text or not creator_id:
        return jsonify({"error": "Both 'text' and 'creator_id' are required."}), 400

    try:
        llm_score = llm_signal(text)
    except Exception as exc:  # noqa: BLE001 - surface any Groq/transport failure
        return jsonify({"error": f"Detection signal failed: {exc}"}), 500

    stylometric_score = stylometric_signal(text)
    confidence = combine_confidence(llm_score, stylometric_score)
    attribution, label = make_label(confidence)

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    conn = get_db()
    try:
        conn.execute(
            """
            INSERT INTO audit_log (
                content_id, creator_id, timestamp, attribution, confidence,
                llm_score, stylometric_score, status,
                appeal_reasoning, appeal_timestamp
            ) VALUES (?, ?, ?, ?, ?, ?, ?, 'classified', NULL, NULL)
            """,
            (
                content_id,
                creator_id,
                timestamp,
                attribution,
                confidence,
                llm_score,
                stylometric_score,
            ),
        )
        conn.commit()
    finally:
        conn.close()

    return jsonify(
        {
            "content_id": content_id,
            "attribution": attribution,
            "confidence": confidence,
            "label": label,
            "signal_scores": {"llm": llm_score, "stylometric": stylometric_score},
        }
    )


@app.route("/log", methods=["GET"])
def get_log():
    conn = get_db()
    try:
        rows = conn.execute(
            "SELECT * FROM audit_log ORDER BY timestamp DESC LIMIT 50"
        ).fetchall()
    finally:
        conn.close()

    return jsonify({"entries": [dict(row) for row in rows]})


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(silent=True) or {}
    content_id = data.get("content_id")
    creator_reasoning = data.get("creator_reasoning")

    if not content_id or not creator_reasoning:
        return (
            jsonify(
                {"error": "Both 'content_id' and 'creator_reasoning' are required."}
            ),
            400,
        )

    conn = get_db()
    try:
        row = conn.execute(
            "SELECT content_id FROM audit_log WHERE content_id = ?",
            (content_id,),
        ).fetchone()
        if row is None:
            return jsonify({"error": "content_id not found"}), 404

        appeal_timestamp = datetime.now(timezone.utc).isoformat()
        conn.execute(
            """
            UPDATE audit_log
            SET status = 'under_review',
                appeal_reasoning = ?,
                appeal_timestamp = ?
            WHERE content_id = ?
            """,
            (creator_reasoning, appeal_timestamp, content_id),
        )
        conn.commit()
    finally:
        conn.close()

    return (
        jsonify(
            {
                "status": "under_review",
                "message": (
                    "Your appeal has been received and logged. A human reviewer "
                    "will examine your submission."
                ),
                "content_id": content_id,
            }
        ),
        200,
    )


init_db()


if __name__ == "__main__":
    app.run(port=5000, debug=True)
